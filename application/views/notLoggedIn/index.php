<p>
	Have you ever started a construction project with a plan in your head that included a predetermined budget, 
	timeline and an image of what your home or office would look like when the work concluded. 
	Exciting thoughts are streaming through your head...
</p>
<p>
	Then reality sets in after the construction starts! The project costs have blown past your budget, 
	the timeline will now impact your holiday plans and your confidence in your chosen contractor have waned beyond repair.  
	And you are constantly wondering, <b>WHAT IS NEXT IN THIS NIGHTMARE?</b>
</p>
<p>
	We realize that most contractors are very good at their jobs, but filtering through the `bad apples` can be quite stressful and costly.  
	We have all been there before, so we created the Fixit Network to alleviate these issues.  
	The Fixit Network is a free application for you and your contractors to use for all remodel and repair projects.  
	Getting started is very simple, just create your own account, enter your contractors contact information and begin organizing your project(s).
</p>
<p>
	If you are a <b>CONTRACTOR</b>, you can also utilize this free application for all your customers whether the project is for a bathroom remodel or developing an apartment complex.  
