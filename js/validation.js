function login_validate() {
	var validator = $( "#login_form" ).validate();

	if(validator.form()) {
		submit.login_submit();
	}
}

function signupValidate() {
	$("#signup_user_form").validate({
		rules: {
			password: {
				required: true
			},
			confirmPassword: {
				required: true,
				equalTo: "#password"
			}
		}
	});

	var validator = $( "#signup_user_form" ).validate();

	if(validator.form()) {
		submit.signupSubmit();
	}
}

